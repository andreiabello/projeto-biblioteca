CREATE TABLE livros(
	id_livro integer NOT NULL,
	titulo varchar(150) NOT NULL,
	autor varchar(100),
	genero varchar(50),
	ano_publicacao integer,
	CONSTRAINT pk_id_livro PRIMARY KEY (id_livro)
);

CREATE TABLE leitores(
	id_leitor integer NOT NULL,
	nome varchar(150) NOT NULL,
	idade integer,
	data_cadastro date,
	CONSTRAINT pk_id_leitor PRIMARY KEY (id_leitor)
);

CREATE TABLE emprestimos(
	id_emprestimo integer NOT NULL,
	id_livro integer NOT NULL,
	id_leitor integer NOT NULL,
	data_emprestimo date,
	data_devolucao date,
	status varchar(20),
	CONSTRAINT pk_id_emprestimos PRIMARY KEY (id_emprestimo),
	CONSTRAINT fk_id_livro FOREIGN KEY (id_livro) REFERENCES livros (id_livro),
	CONSTRAINT fk_id_leitor FOREIGN KEY (id_leitor) REFERENCES leitores (id_leitor)
);

